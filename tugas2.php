<?php
$P = "10";
$L = "9";

// Operator / → digunakan untuk pembagian, hasilnya bisa berupa bilangan pecahan (float).
echo "Hasil pembagian \$P / \$L = " . ($P / $L); // 1.111...
echo "<br />";

// Operator % (modulo) → digunakan untuk mencari sisa hasil bagi, hasilnya selalu bilangan bulat (integer).
echo "Hasil modulo \$P % \$L = " . ($P % $L); // 1
echo "<br />";
?>